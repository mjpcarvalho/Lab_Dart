// FOR
void main() {
  for (int i = 0; i <= 5; i++) {
    print(i);
  }

// Outro exemplo

  for (int x = 1; x <= 20; x++) {
    print(x);
  }

// incrementar de dois em dois

  for (int v = 10; v <= 20; v += 2) {
    print(v);
  }
// decrementando um valor de dois em dois
  for (int beta = 20; beta >= 0; beta -= 2) {
    print(beta);
  }

  for (int alpha = 2; alpha <= 50000; alpha *= 2) {
    print(alpha);
  }
}
