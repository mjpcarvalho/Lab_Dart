main() {
  // String

  String nome_usuario = 'João';
  String sobrenome_usuario = 'Carvalho';
  print(nome_usuario); // João
  print(sobrenome_usuario); // Carvalho

  // Upercase

  nome_usuario = nome_usuario.toUpperCase();
  print(nome_usuario);
  sobrenome_usuario = sobrenome_usuario.toUpperCase();
  print(sobrenome_usuario);

  // Lowercase

  nome_usuario = nome_usuario.toLowerCase();
  print(nome_usuario);
  sobrenome_usuario = sobrenome_usuario.toLowerCase();
  print(sobrenome_usuario);
}
